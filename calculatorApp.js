var calculator = {
    add : function (x, y) {
        return x + y;
    },
    sub : function (x, y) {
        return x - y;
    },
    mul : function (x, y) {
        return x * y;
    },
    div : function (x, y) {
        return x / y;
    },
};

console.log(calculator.add(2, 4));
console.log(calculator.sub(2, 4));
console.log(calculator.mul(2, 4));
console.log(calculator.div(2,4));
